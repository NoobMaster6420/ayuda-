// LaTeX Component
interface LatexProps {
  formula: string;
  displayMode?: boolean;
  className?: string;
}

function Latex({ formula, displayMode = true, className = '' }: LatexProps) {
  const ref = useRef<HTMLDivElement>(null);

  useEffect(() => {
    if (ref.current && formula) {
      katex.render(formula, ref.current, {
        throwOnError: false,
        displayMode,
      });
    }
  }, [formula, displayMode]);

  return <div ref={ref} className={className} />;
}

// BackgroundMusic Component
interface BackgroundMusicProps {
  src: string;
  autoPlay?: boolean;
  loop?: boolean;
  volume?: number;
}

function BackgroundMusic({ 
  src,
  autoPlay = true,
  loop = true,
  volume = 0.3 
}: BackgroundMusicProps) {
  return (
    <ReactAudioPlayer
      src={src}
      autoPlay={autoPlay}
      loop={loop}
      volume={volume}
      style={{ display: 'none' }}
    />
  );
}

